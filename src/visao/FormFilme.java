    private void jTableTabelaFilmeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTabelaFilmeMouseClicked
        String nome = ""+jTableTabelaFilme.getValueAt(jTableTabelaFilme.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select * from filmes where titulo='"+ nome +"'");
        try{
            conex.rs.first();
            jTextFieldID.setText(String.valueOf(conex.rs.getInt("id_filme")));
            jTextFieldTitulo.setText(String.valueOf(conex.rs.getString("titulo")));
            jTextFieldPrecoFilme.setText(String.valueOf(conex.rs.getString("valor_entrada")));
            jTextFieldGenero.setText(String.valueOf(conex.rs.getString("genero")));
            jTextFieldSinopse.setText(String.valueOf(conex.rs.getString("sinopse_filme")));
        }catch(SQLException ex){
            System.out.println("Erro ao Selecionar Dados: " +ex.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao Selecionar Dados!");
        }
        conex.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonDeletar.setEnabled(true);
    }//GEN-LAST:event_jTableTabelaFilmeMouseClicked
    
    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Título", "Entrada", "Gênero"};
        conex.conexao();
        conex.executaSql(Sql);
        
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("id_filme"), conex.rs.getString("titulo"), conex.rs.getDouble("valor_entrada"), conex.rs.getString("genero")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            System.out.println("Erro ao Preencher ArrayList!"+ ex.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Nenhum Filme Corresponde à Pesquisa!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableTabelaFilme.setModel(modelo);
        jTableTabelaFilme.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTableTabelaFilme.getColumnModel().getColumn(0).setResizable(false);
        jTableTabelaFilme.getColumnModel().getColumn(1).setPreferredWidth(205);
        jTableTabelaFilme.getColumnModel().getColumn(1).setResizable(false);
        jTableTabelaFilme.getColumnModel().getColumn(2).setPreferredWidth(55);
        jTableTabelaFilme.getColumnModel().getColumn(2).setResizable(false);
        jTableTabelaFilme.getColumnModel().getColumn(3).setPreferredWidth(165);
        jTableTabelaFilme.getColumnModel().getColumn(3).setResizable(false);
        jTableTabelaFilme.getTableHeader().setReorderingAllowed(false);
        jTableTabelaFilme.setAutoResizeMode(jTableTabelaFilme.AUTO_RESIZE_OFF);
        jTableTabelaFilme.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    }
