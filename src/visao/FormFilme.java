    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jTextFieldTitulo.getText().isEmpty() || jTextFieldGenero.getText().isEmpty() || jTextFieldPrecoFilme.getText().isEmpty() || jTextFieldSinopse.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar");
            //jTextFieldNome.requestFocus();
        }else{
            if(flag == 1){
                mod.setTituloFilme(jTextFieldTitulo.getText());
                mod.setGeneroFilme(jTextFieldGenero.getText());
                mod.setSinopseFilme(jTextFieldSinopse.getText());
                mod.setValorEntrada(Double.parseDouble(jTextFieldPrecoFilme.getText()));
                dao.SalvarFilme(mod);
                jTextFieldTitulo.setText("");
                jTextFieldGenero.setText("");
                jTextFieldSinopse.setText("");
                jTextFieldPrecoFilme.setText("");
                jTextFieldTitulo.setEnabled(true);
                jTextFieldGenero.setEnabled(true);
                jTextFieldSinopse.setEnabled(true);
                jTextFieldPrecoFilme.setEnabled(true);
                jTextFieldPesquisar.setEnabled(true);
                jButtonSalvar.setEnabled(false);
                jButtonCancelar.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonBuscar.setEnabled(true);
                jTextFieldTitulo.setText("");
                jTextFieldID.setText("");
                jTextFieldPrecoFilme.setText("");
                jTextFieldGenero.setText("");
                jTextFieldSinopse.setText("");
                preencherTabela("select * from filmes order by titulo");
            }else{
                mod.setIdFilme(Integer.parseInt(jTextFieldID.getText()));
                mod.setTituloFilme(jTextFieldTitulo.getText());
                mod.setValorEntrada(Double.parseDouble(jTextFieldPrecoFilme.getText()));
                mod.setGeneroFilme(jTextFieldGenero.getText());
                mod.setSinopseFilme(jTextFieldSinopse.getText());
                dao.EditarFilme(mod);
                jTextFieldID.setText("");
                jTextFieldTitulo.setText("");
                jTextFieldPrecoFilme.setText("");
                jTextFieldGenero.setText("");
                jTextFieldSinopse.setText("");
                jTextFieldTitulo.setEnabled(false);
                jTextFieldPrecoFilme.setEnabled(false);
                jTextFieldGenero.setEnabled(false);
                jTextFieldSinopse.setEnabled(false);
                jTextFieldPesquisar.setEnabled(true);
                jButtonBuscar.setEnabled(true);
                jButtonSalvar.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonCancelar.setEnabled(false);
                preencherTabela("select * from filmes order by titulo");
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

