    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"Código", "Nome", "Idade", "Tipo"};
        conex.conexao();
        conex.executaSql(Sql);
        
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("cod_usuario"), conex.rs.getString("nome_usuario"), conex.rs.getString("idade"), conex.rs.getString("usu_tipo")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            System.out.println("Erro ao Preencher ArrayList!"+ ex.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Nenhum Usuário Corresponde à Pesquisa!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableUsuario.setModel(modelo);
        jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableUsuario.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(2).setPreferredWidth(20);
        jTableUsuario.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTableUsuario.getColumnModel().getColumn(3).setResizable(false);
        //jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF);
        jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    }
    
