    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jTextFieldUsuario.getText().isEmpty() || jTextFieldIdade.getText().isEmpty() || jTextFieldNumCartao.getText().isEmpty() || jPasswordFieldSenha.getText().isEmpty() || jPasswordFieldConfSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar!");
        }else{
            if(jPasswordFieldSenha.getText().equals(jPasswordFieldConfSenha.getText())){
                if(flag == 1){
                    mod.setUsuNome(jTextFieldUsuario.getText());
                    mod.setUsuIdade(Integer.parseInt(jTextFieldIdade.getText()));
                    mod.setNumCartao(Integer.parseInt(jTextFieldNumCartao.getText()));
                    mod.setUsuSenha(jPasswordFieldSenha.getText());
                    mod.setUsuTipo((String) jComboBoxTipo.getSelectedItem());
                    mod.setValorConta(0); //Valor padrão ao criar uma nova conta

                    dao.Salvar(mod);
                    preencherTabela("select * from usuarios order by nome_usuario");

                    jTextFieldUsuario.setText("");
                    jTextFieldIdade.setText("");
                    jTextFieldNumCartao.setText("");
                    jPasswordFieldSenha.setText("");
                    jPasswordFieldConfSenha.setText("");
                    jTextFieldUsuario.setEnabled(false);
                    jTextFieldIdade.setEnabled(false);
                    jTextFieldNumCartao.setEnabled(false);
                    jPasswordFieldSenha.setEnabled(false);
                    jPasswordFieldConfSenha.setEnabled(false);
                    jComboBoxTipo.setEnabled(false);
                    jButtonSalvar.setEnabled(false);
                    jButtonCancelar.setEnabled(false);
                    jButtonNovo.setEnabled(true);

                }else{
                    mod.setUsuNome(jTextFieldUsuario.getText());
                    mod.setUsuIdade(Integer.parseInt(jTextFieldIdade.getText()));
                    mod.setNumCartao(Integer.parseInt(jTextFieldNumCartao.getText()));
                    mod.setUsuSenha(jPasswordFieldSenha.getText());
                    mod.setUsuTipo((String) jComboBoxTipo.getSelectedItem());
                    mod.setValorConta(Double.parseDouble(jTextFieldValorConta.getText()));
                    mod.setCodUsuario(Integer.parseInt(""+jTableUsuario.getValueAt(jTableUsuario.getSelectedRow(), 0)));

                    dao.Editar(mod);
                    preencherTabela("select * from usuarios order by nome_usuario");

                    jTextFieldUsuario.setText("");
                    jTextFieldIdade.setText("");
                    jTextFieldNumCartao.setText("");
                    jPasswordFieldSenha.setText("");
                    jPasswordFieldConfSenha.setText("");
                    jTextFieldUsuario.setEnabled(false);
                    jTextFieldIdade.setEnabled(false);
                    jTextFieldNumCartao.setEnabled(false);
                    jPasswordFieldSenha.setEnabled(false);
                    jPasswordFieldConfSenha.setEnabled(false);
                    jComboBoxTipo.setEnabled(false);
                    jButtonSalvar.setEnabled(false);
                    jButtonCancelar.setEnabled(false);
                    jButtonNovo.setEnabled(true);
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Senhas Diferentes. Digite Senha Iguais!");
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

