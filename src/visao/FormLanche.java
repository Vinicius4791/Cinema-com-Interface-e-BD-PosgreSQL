    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Nome", "Pre√ßo", "Ingredientes"};
        conex.conexao();
        conex.executaSql(Sql);
        
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("id_lanche"), conex.rs.getString("nome_lanche"), conex.rs.getDouble("preco_lanche"), conex.rs.getString("ingredientes_lanche")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            System.out.println("Erro ao Preencher ArrayList!"+ ex.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Nenhum Lanche Corresponde √† Pesquisa!");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableCadLanches.setModel(modelo);
        jTableCadLanches.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTableCadLanches.getColumnModel().getColumn(0).setResizable(false);
        jTableCadLanches.getColumnModel().getColumn(1).setPreferredWidth(165);
        jTableCadLanches.getColumnModel().getColumn(1).setResizable(false);
        jTableCadLanches.getColumnModel().getColumn(2).setPreferredWidth(55);
        jTableCadLanches.getColumnModel().getColumn(2).setResizable(false);
        jTableCadLanches.getColumnModel().getColumn(3).setPreferredWidth(250);
        jTableCadLanches.getColumnModel().getColumn(3).setResizable(false);
        jTableCadLanches.getTableHeader().setReorderingAllowed(false);
        jTableCadLanches.setAutoResizeMode(jTableCadLanches.AUTO_RESIZE_OFF);
        jTableCadLanches.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    }
    
