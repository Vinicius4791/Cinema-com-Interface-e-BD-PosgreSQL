    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jTextFieldNomeLanche.getText().isEmpty() || jTextFieldIngredientesLanche.getText().isEmpty() || jTextFieldPrecoLanche.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para continuar");
            //jTextFieldNome.requestFocus();
        }else{
            if(flag == 1){
                mod.setNomeLanche(jTextFieldNomeLanche.getText());
                mod.setIngredientesLanche(jTextFieldIngredientesLanche.getText());
                mod.setPrecoLanche(Double.parseDouble(jTextFieldPrecoLanche.getText()));
                dao.Salvar(mod);
                jTextFieldNomeLanche.setText("");
                jTextFieldIngredientesLanche.setText("");
                jTextFieldPrecoLanche.setText("");
                jTextFieldNomeLanche.setEnabled(true);
                jTextFieldPrecoLanche.setEnabled(true);
                jTextFieldIngredientesLanche.setEnabled(true);
                jTextFieldPesquisar.setEnabled(true);
                jButtonSalvar.setEnabled(false);
                jButtonCancelar.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonBuscar.setEnabled(true);
                jTextFieldNomeLanche.setText("");
                jTextFieldIDLanche.setText("");
                jTextFieldPrecoLanche.setText("");
                jTextFieldIngredientesLanche.setText("");
                preencherTabela("select * from lanches order by nome_lanche");
            }else{
                mod.setCodLanche(Integer.parseInt(jTextFieldIDLanche.getText()));
                mod.setNomeLanche(jTextFieldNomeLanche.getText());
                mod.setPrecoLanche(Double.parseDouble(jTextFieldPrecoLanche.getText()));
                mod.setIngredientesLanche(jTextFieldIngredientesLanche.getText());
                dao.Editar(mod);
                jTextFieldIDLanche.setText("");
                jTextFieldNomeLanche.setText("");
                jTextFieldPrecoLanche.setText("");
                jTextFieldIngredientesLanche.setText("");
                jTextFieldNomeLanche.setEnabled(false);
                jTextFieldPrecoLanche.setEnabled(false);
                jTextFieldIngredientesLanche.setEnabled(false);
                jTextFieldPesquisar.setEnabled(true);
                jButtonBuscar.setEnabled(true);
                jButtonSalvar.setEnabled(false);
                jButtonNovo.setEnabled(true);
                jButtonCancelar.setEnabled(false);
                preencherTabela("select * from lanches order by nome_lanche");
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

