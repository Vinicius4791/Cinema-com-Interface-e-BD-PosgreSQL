    private void jTableCadLanchesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCadLanchesMouseClicked
        String nome = ""+jTableCadLanches.getValueAt(jTableCadLanches.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select * from lanches where nome_lanche='"+ nome +"'");
        try{
            conex.rs.first();
            jTextFieldIDLanche.setText(String.valueOf(conex.rs.getInt("id_lanche")));
            jTextFieldNomeLanche.setText(String.valueOf(conex.rs.getString("nome_lanche")));
            jTextFieldPrecoLanche.setText(String.valueOf(conex.rs.getString("preco_lanche")));
            jTextFieldIngredientesLanche.setText(String.valueOf(conex.rs.getString("ingredientes_lanche")));
        }catch(SQLException ex){
            System.out.println("Erro ao Selecionar Dados: " +ex.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao Selecionar Dados!");
        }
        conex.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonDeletar.setEnabled(true);
    }//GEN-LAST:event_jTableCadLanchesMouseClicked

